    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
        // obteniendo los datos del usuario
        String email = this.email.getText();
        String password = this.password.getText();
        
        // creando instancia de empleado
        Employee employee = new Employee(email, password);
        
        // enviando datos del empleado al controlador
        EmployeeController controller = new EmployeeController();
        employee = controller.login(employee);
        
        if(employee.getId_rol() == 1){
            this.dispose();
            PanelAdmin admin = new PanelAdmin(employee);
            admin.setVisible(true);
        }else if(employee.getId_rol() == 2){
            this.dispose();
            PanelEmployee panel = new PanelEmployee(employee);
            panel.setVisible(true);
        }
        
        // mostrando mensajes
        alert.setText(employee.getAlert());
    }//GEN-LAST:event_loginMouseClicked

